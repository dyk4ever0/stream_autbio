import streamlist as st

# Title (largest size)
st.title("Title")

# Header (second largest size)
st.header("Header")

# Subheader (third largest size)
st.subheader("Subheader")

# Markdown (customizable size using Markdown formatting)
st.markdown("# Markdown (h1 - largest)")
st.markdown("## Markdown (h2 - second largest)")
st.markdown("### Markdown (h3 - third largest)")
st.markdown("#### Markdown (h4 - fourth largest)")
st.markdown("##### Markdown (h5 - fifth largest)")
st.markdown("###### Markdown (h6 - smallest)")

# Text (smallest size, plain text)
st.text("Text (smallest size)")
